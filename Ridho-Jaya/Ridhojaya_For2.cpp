#include <iostream>

using namespace std;

int main() {
    // Loop pertama untuk variabel x
    for (int x = 0; x < 3; x++) {
        // Loop kedua untuk variabel y
        for (int y = 0; y < 3; y++) {
            // Menampilkan nilai x dan y ke layar
            cout << "x = " << x << ", y = " << y << endl;
        }
    }

    return 0;
}
