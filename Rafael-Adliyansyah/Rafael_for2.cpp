#include <iostream>

using namespace std;

int main() {
    // Contoh for loop untuk mencetak angka 1 hingga 5
    for (int i = 1; i <= 5; i++) {
        cout << "Nilai i: " << i << endl;
    }

    // Contoh for loop untuk mencetak angka 10 hingga 1 dengan langkah mundur
    for (int j = 10; j >= 1; j--) {
        cout << "Nilai j: " << j << endl;
    }

    return 0;
}